#!/usr/bin/env bash
###############################################################################
#
# Foundry Bootstrap
#
# Stage 00b
#
# Normalize GitHub remote using gh authentication.
#
###############################################################################

set -euo pipefail

ORG="project-ilm"
REPO="foundry"

cd ~/work/foundry

echo
echo "======================================================"
echo "Foundry Remote Normalization"
echo "======================================================"

###############################################################################
# Verify authentication
###############################################################################

gh auth status

###############################################################################
# Repository must exist
###############################################################################

gh repo view "${ORG}/${REPO}" >/dev/null

###############################################################################
# Canonical HTTPS URL
###############################################################################

REMOTE_URL="$(gh repo view "${ORG}/${REPO}" --json url -q .url).git"

echo
echo "Remote:"
echo "  ${REMOTE_URL}"

###############################################################################
# Configure origin
###############################################################################

if git remote get-url origin >/dev/null 2>&1
then
    git remote set-url origin "${REMOTE_URL}"
else
    git remote add origin "${REMOTE_URL}"
fi

###############################################################################
# Current branch
###############################################################################

BRANCH="$(git branch --show-current)"

echo
echo "Branch : ${BRANCH}"

###############################################################################
# Push
###############################################################################

git push -u origin "${BRANCH}"

###############################################################################
# Show final state
###############################################################################

echo
git remote -v

echo
git branch -vv

echo
echo "SUCCESS"

